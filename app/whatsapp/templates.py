        "أنا مساعدك الذكي 🤖، ومهمتي أخلي تجربتك عندنا مريحة وتليق بمقامك.\n\n"
        "لخدمتك بشكل أسرع، الرجاء كتابة *الرقم* المناسب لطلبك:\n"
        "1️⃣ *الاستعلام:* أشيّك لك على الغرف الفاضية والأسعار\n"
        "2️⃣ *الحجوزات:* أسجل لك حجز جديد برقم تأكيد فوري\n"
        "3️⃣ *الخدمات:* أطلب لك خدمة غرف أو أي طلب ثاني\n"
        "4️⃣ *الملاحظات:* أرفع عنك شكوى أو اقتراح للإدارة\n\n"
